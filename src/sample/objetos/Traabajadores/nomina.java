    public void setDia(Integer dia) {
        this.dia = dia;
    }

    public Integer getPrestamo() {
        return prestamo;
    }

    public void setPrestamo(Integer prestamo) {
        this.prestamo = prestamo;
    }

    public Integer getAhorro() {
        return ahorro;
    }

    public void setAhorro(Integer ahorro) {
        this.ahorro = ahorro;
    }

